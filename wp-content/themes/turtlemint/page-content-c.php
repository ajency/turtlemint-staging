				<div class="listing-item">
					<h3 class="plan-name">Endowment plans: </h3>
					<p class="plan-text">Endowment plans are savings-oriented life insurance plans. These plans have both death benefit as well as maturity benefit.</p>
					<p class="plan-text">Death Benefit is paid to the nominee if the life insured dies within the policy tenure and Maturity Benefit is paid to the policyholder if he survives the entire policy tenure.</p>
				</div>
				<div class="listing-item">
					<h3 class="plan-name">ULIPS: </h3>
					<p class="plan-text">Unit Linked insurance plans (ULIPs) are investment oriented insurance plans. Under these plans, the premiums paid are invested in the capital market. There are different types of investment funds and the policyholder can choose the fund in which the premium should be invested. ULIPs allow fund option to choose from equity, debt, balanced, liquid etc.</p>
				</div>
				<div class="listing-item">
					<h3 class="plan-name">Child Insurance plans: </h3>
					<p class="plan-text">Child plans are life insurance plans created specifically for providing financial stability to the child. The parent or the child is the life insured under the plan. Usually, the parent is covered in a child plan. </p>
					<p class="plan-text">There are 2 types of child insurance plans: </p>
					<ul class="list-style text left">
						<li class="plan-text">One being the traditional child insurance plans with maturity, bonus (if applicable) and death benefit, if the parent dies.</li >
						<li class="plan-text">Unit-linked child plans with maturity (investment premium + growth) and a death benefit if the parent dies.</li >
					</ul>
				</div>
				<div class="listing-item">
					<div class="plan-name">Whole life plan:</div>
					<p class="plan-text">Whole Life Plans are unlimited Term Plans. These plans cover individuals till 99 or 100 years of age.</p>
