									<p class="paragraph-text ">Life insurance policies offer the following common riders -</p>
									<ul class="list-style mtop0">
										<li class="li paragraph-text">Accidental death and disablement benefit rider - under this rider, an additional sum assured is paid if the insured dies or  becomes permanently disabled during the term of the plan due to an accident</li>
										<li class="li paragraph-text">Premium waiver rider - under this rider, future premiums payable under the plan are waived off if the policyholder dies during the term of the plan or meets with an accident or a critical illness, as is mentioned in the policy bond. In that case, the future premiums are waived off and the policy continues. The policy matures in its scheduled time.</li>
										<li class="li paragraph-text">Critical illness rider - this rider covers a list of critical illnesses. If the insured is diagnosed with any of the covered illnesses during the term of the plan, an additional sum assured is paid</li>
										<li class="li paragraph-text">Hospital cash rider - under this rider, a daily cash benefit is paid if the insured is hospitalised during the term of the plan</li>
										<li class="li paragraph-text">Term rider - if the insured dies during the term of the plan, double sum assured is paid irrespective of the cause of death</li>
									</ul>
									<p class="paragraph-text ">Any one or multiple riders can be chosen along with the basic policy.</p>
