			<div class="SEO-content-text">
				<ol>
					<li>
						<b>Insurance is NOT an investment</b><br>This is the first thing you need to acknowledge and accept that insurance should never be considered as an investment. Insurance is a vital part of financial planning. But a lot of people consider it as an investment.<br>
						This is primarily because there are some life insurance plans in India that double up as investments.  For example, Endowment Policies have a lump-sum maturity benefit, Money Back Plans have regular payments during the entire policy tenure as pre-defined schedule and Unit Linked Insurance Plans have an opportunity to choose your investments even in equity! But what you need to keep in mind while choosing your Life Insurance Policy is the coverage amount you wish to purchase for your family. Insurance is synonymous to protection and that is the primary and most important objective of ANY life insurance product. They should not be evaluated on the basis of their return, bonus, etc.
					</li>
					<li>
						<b>Required Coverage amount</b><br>The required coverage amount for your insurance policy is the most important aspect of choosing a plan. It depends on your lifestyle and priorities. You have to consider your standard of living, inflation, needs, liabilities etc. Then, accordingly, decide your coverage amount which would be sufficient for your family.<br>
						For instance, if your family’s monthly expenses are say ₹50,000. You need to opt for a Life Insurance coverage of at least ₹ 1 crore so that a post-tax interest earning is enough to provide for the family’s regular monthly expenses! So, ensure that your coverage amount is sufficient to meet the monthly expense of your family in your absence.
					</li>
					<li>
						<b>Policy Tenure</b><br>The tenure of Life insurance policies depends on the type of plan you opt for. You can choose any tenure that you think is necessary for you and your family. However, the best way to select an apt tenure for your insurance policy is as long as you are earning and providing for your family. This is typically till the age of 60 or 65 years post which your children will definitely start earning and so your financial dependence will gradually reduce!
					</li>
					<li>
						<b>Type of Life Insurance best suited for you</b></br>There are varieties of plans offered and available in the market. It all depends on your needs as to which plan you opt for. Some of the insurance plans are:
