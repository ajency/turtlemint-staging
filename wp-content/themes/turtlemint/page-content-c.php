									<p class="paragraph-text">Money back plans are anticipated endowment plans. They also have death as well as maturity benefit like Regular Endowment Plans, with the only difference that rather than paying the sum assured in one lump sum on maturity, money back plans pay a portion of the sum assured periodically during the policy tenure. These plans, thus, provide liquidity. The survival, maturity and death benefits are given below:</p>
									<p class="paragraph-text"><b>a.Survival Benefit</b>= % of the Sum Assured as per the policy schedule on the pre-defined year. For example, 10% of the Sum Assured is paid every 5 years, i.e. on survival, </p>
									<ul class="list-style">
										<li class="li paragraph-text">On the 5th year, 10% of the Sum Assured would be paid</li>
										<li class="li paragraph-text">On the 10th year, another 10% of the Sum Assured would be paid</li>
										<li class="li paragraph-text">On the 15th year, another 10% of the Sum Assured would be paid</li>
										<li class="li paragraph-text">And on the 20th year, another 10% of the Sum Assured would be paid</li>
									</ul>
									<p class="paragraph-text"><b>b.Maturity Benefit</b>= Remaining Sum Assured + Bonus (if applicable) is paid to the policyholder if the life insured survives the entire policy tenure and the policy ends.</p>
									<p class="paragraph-text">Thus, in the previous example, the remaining 60% of the Sum Assured + Bonus (if applicable) would be paid at the end of the policy tenure as Maturity Benefit.</p>
									<p class="paragraph-text"><b>c. Death Benefit</b>= Sum Assured + Bonus (if applicable) is paid to the nominee if the life insured dies within the policy tenure irrespective of the amount already paid as survival benefit and the policy ends.</p>
									<p class="paragraph-text">Bonus declarations, guaranteed additions or loyalty additions might also be paid with the death or maturity benefit, if applicable.</p>
								</div>
