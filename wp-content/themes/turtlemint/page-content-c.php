								<div class="panel-body">
									<p class="paragraph-text">There are 2 types of pension plans namely Deferred pension plans and immediate annuity pension plans, the details are as mentioned below:</p>
									<p class="paragraph-text"><b>a. Deferred pension plans </b>– under these plans, </p>
									<ul class="list-style">
										<li class="li paragraph-text">The policyholder chooses a policy term and pays premiums during the term. These premiums get accumulated into a lump sum benefit. Pension starts from the date of Vesting.</li>
										<li class="li paragraph-text"><strong>Death benefit before Vesting:</strong> In case of early death, the death benefit is paid</li>
										<li class="li paragraph-text"><strong>On Vesting:</strong> At the time of vesting, the annuitant can withdraw 1/3rd of the accumulated corpus as tax-free withdrawal and  the remaining portion is then paid as annuity according to the annuity option selected.</li>
										<li class="li paragraph-text"><strong>Death benefit after Vesting:</strong> It depends on the annuity option chosen. If the annuitant has chosen, return of purchase price option, then the same would be paid to the nominee.</li>
									</ul>
									<p class="paragraph-text"><b>b. Immediate annuity plans </b>– under these plans, </p>
									<ul class="list-style">
										<li class="li paragraph-text">The policyholder pays a lump sum amount to buy the plan and the annuity starts immediately according to the annuity option selected.</li>
										<li class="li paragraph-text"><b>Death benefit after Vesting:</b> It depends on the annuity option chosen. If the annuitant has chosen, return of purchase price option, then the same would be paid to the nominee.</li>
