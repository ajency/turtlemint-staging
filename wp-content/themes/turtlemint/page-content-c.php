							<li>Term Insurance</li>
							<li>Whole life insurance plans</li>
							<li>Endowment Assurance Plans</li>
							<li>Money-Back Plans</li>
							<li>Child Plans</li>
							<li>Pension Plans</li>
							<li>Unit Linked Insurance Plan</li>
						</ol>
