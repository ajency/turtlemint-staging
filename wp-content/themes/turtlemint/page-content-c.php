					<p class="plan-text">Term Insurance plans provide a death benefit for a fixed tenure. This is the most basic and also the most important type of life insurance policy available in the market. </p>
					<p class="plan-text">There are certain sub-types of term life plans like level term plans, increasing term plans, decreasing term plans & term plan with return of premium.</p>
