			<h2 class="adds-heading">Benefits of Health Insurance Plans &amp; Policies</h2>
			<div class="adds-recomment max-width-750">Medical insurance policies offer various advantages and come with unique features which are as follows &ndash;
				<ul  class="adds-recomment max-width-750">
					<li>Medical insurance plans cover all medical costs incurred right from the time the insured falls sick to his hospitalisation and also after being discharged from the hospital. Thus, the plan has a wide scope of coverage for medical costs</li>
					<li>There are value-added coverage benefits in medical insurance policies too. These include free health check-ups after a specified period, second medical opinion for serious illnesses, etc.</li>
					<li>Health plans allow tax advantages. Premiums paid for medical insurance policies for self, family and dependent parents qualify for tax deduction under Section 80D. The limit of deduction if INR 25, 000 for covering self and family and another INR 25, 000 for covering dependent parents. Moreover, if either the policyholder and/or dependent parents are senior citizens, the maximum limit increases to INR 50, 000 in each instance. Thus, a maximum of INR 1 lakh can be claimed as tax deduction though health insurance plans</li>
					<li>No Claim bonus is allowed in all medical insurance plans if no claim is made in a policy year. This bonus is either allowed as an increase in the sum insured or additional benefits can be availed like gift vouchers, annual health check-ups, etc.&nbsp;</li>
					<li>Lifelong renewals are offered by health plans with no maximum cover ceasing age</li>
					<li>The term of the plan can be for one, two or three years. Moreover, if long term plans are chosen, a premium discount is also allowed</li>
					<li>Individuals can claim premium discounts for covering two or more family members, by choosing a longer duration, by choosing voluntary co-payment, etc.</li>
					<li>Cashless claims are settled by the health insurance company if the policyholder seeks treatments at a hospital which is tied-up with the insurance company. In cashless claims, the policyholder does not have to bear the burden of medical expenses. The expenses are settled directly by the insurance company with the hospital.</li>
